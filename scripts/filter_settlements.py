def is_russian_geo_name(name):
    if len(name) < 3:
        return False
    if re.match(r"^[0-9]", name):
        return False
    if re.match(r"^[a-zA-Z]", name):
        return False
    if any(c in name for c in '↑↓←→≈{}[]|\\<>/^~`@#$%&*+=!?:;"'):
        return False
    if re.search(r"\d", name):
        return False
    for sw in SUSPICIOUS_WORDS:
        if sw.lower() in name.lower() and len(name) < len(sw) + 5:
            return False
    if name in COMMON_NON_GEO:
        return False
    words = name.split()
    if len(words) == 1:
        if len(name) > 25:
            return False
        if name[0].islower():
            return False
        if not re.match(r"^[А-ЯЁ]", name):
            return False
    return True


